#include <stdio.h>

/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: final answer
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: final answer
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod of two integers
 * @a: first integer
 * @b: second integer
 * Return: final answer
 */
int mod(int a, int b)
{
	return(a % b);
}

/**
 * sub - subtract two intergers 
 * @a: first integer
 * @b: second integer
 * Return: final answer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: final answer
 */
int mul(int a, int b)
{
	return (a * b);
}
